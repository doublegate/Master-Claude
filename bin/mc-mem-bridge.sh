#!/bin/sh
# mc-mem-bridge.sh — make the shared memory-core facts available to Codex and Gemini.
# Builds a single consolidated digest from memory-core/ and writes it into each agent's home
# (default ~/.codex and ~/.gemini) as `master-memory.md`, so Codex/Gemini can reference the
# same curated facts Claude gets (today only the instruction file is shared across agents).
# DRY-RUN by default; pass --execute to write. Idempotent (regenerates the digest).
#
# Usage: mc-mem-bridge.sh [--execute] [--codex-home DIR] [--gemini-home DIR] [--only codex|gemini]
set -eu

SCRIPT_DIR=$(CDPATH='' cd -- "$(dirname -- "$0")" && pwd)
REPO_ROOT=$(CDPATH='' cd -- "$SCRIPT_DIR/.." && pwd)
MEMCORE="$REPO_ROOT/memory-core"

EXECUTE=0
CODEX_HOME="$HOME/.codex"
GEMINI_HOME="$HOME/.gemini"
ONLY=""

while [ $# -gt 0 ]; do
  case "$1" in
    --execute) EXECUTE=1; shift ;;
    --codex-home) [ $# -gt 1 ] || { printf 'mc-mem-bridge: --codex-home requires an argument\n' >&2; exit 1; }
                  CODEX_HOME="$2"; shift 2 ;;
    --gemini-home) [ $# -gt 1 ] || { printf 'mc-mem-bridge: --gemini-home requires an argument\n' >&2; exit 1; }
                   GEMINI_HOME="$2"; shift 2 ;;
    --only) [ $# -gt 1 ] || { printf 'mc-mem-bridge: --only requires an argument\n' >&2; exit 1; }
            ONLY="$2"; shift 2 ;;
    -h|--help) sed -n '2,10p' "$0"; exit 0 ;;
    *) printf 'mc-mem-bridge: unknown arg: %s\n' "$1" >&2; exit 1 ;;
  esac
done
[ -d "$MEMCORE" ] || { printf 'mc-mem-bridge: no memory-core at %s\n' "$MEMCORE" >&2; exit 1; }

strip_fm() { awk 'NR==1 && $0=="---"{fm=1;next} fm==1 && $0=="---"{fm=0;next} fm!=1{print}' "$1"; }

DIGEST=$(mktemp); trap 'rm -f "$DIGEST"' EXIT
{
  printf '# Master-Claude — shared memory digest\n\n'
  printf '%s\n\n' "> Generated by mc-mem-bridge from memory-core/. Curated, cross-project facts so"
  printf '%s\n\n' "> Codex/Gemini share what Claude knows. Reference this file from your global agent"
  printf '%s\n\n' "> config (Codex AGENTS.md / Gemini GEMINI.md), or paste the relevant entries in."
  for f in "$MEMCORE"/*.md; do
    [ -e "$f" ] || continue
    b=$(basename -- "$f"); [ "$b" = MEMORY.md ] && continue
    name=$(sed -n 's/^name:[[:space:]]*//p' "$f" | head -n 1)
    [ -n "$name" ] || name=$(basename -- "$f" .md)
    printf '## %s\n\n' "$name"
    strip_fm "$f" | sed '/^[[:space:]]*$/{N;/^[[:space:]]*\n[[:space:]]*$/D}'
    printf '\n'
  done
} > "$DIGEST"

facts=0
for f in "$MEMCORE"/*.md; do
  [ -e "$f" ] || continue
  [ "${f##*/}" = MEMORY.md ] && continue
  facts=$((facts+1))
done
lines=$(wc -l < "$DIGEST" | tr -d ' ')

mode="DRY-RUN (no files written; pass --execute)"
[ "$EXECUTE" -eq 1 ] && mode="EXECUTE"
printf 'mc-mem-bridge: %s — %s facts -> %s-line digest\n' "$mode" "$facts" "$lines"

write_one() {
  home="$1"
  dest="$home/master-memory.md"
  if [ "$EXECUTE" -eq 1 ]; then
    mkdir -p "$home"
    cp "$DIGEST" "$dest"
    printf '  wrote %s\n' "$dest"
  else
    printf '  would write %s\n' "$dest"
  fi
}

case "$ONLY" in
  codex)  write_one "$CODEX_HOME" ;;
  gemini) write_one "$GEMINI_HOME" ;;
  "")     write_one "$CODEX_HOME"; write_one "$GEMINI_HOME" ;;
  *) printf 'mc-mem-bridge: --only must be codex|gemini\n' >&2; exit 1 ;;
esac

if [ "$EXECUTE" -eq 0 ]; then
  printf 'Re-run with --execute to write. Then add to your global Codex/Gemini config:\n'
  printf '  "See master-memory.md in this config directory for shared project conventions."\n'
fi
